package com.example.android.dotsandboxes;

public class Dot {

    private float xOfDot;
    private float yOfDot;

    public Dot(float x, float y) {
        xOfDot = x;
        yOfDot = y;
    }

    public float getxOfDot() {
        return xOfDot;
    }

    public float getyOfDot() {
        return yOfDot;
    }
}
